/**
 * ir al index clicando en el logo de ajebask
 */
function goIndex() {
    window.location.href = "../../index.php";
}

/**
 * ir a mis anuncios al darle el boton mis anuncios el los lugares donde no son el index
 */
function goMisAnuncios() {
    window.location.href = "busqueda.php?action=misAnuncios";
}

/**
 * ir a publicar anuncio en los lugares donde no son el index
 */
function goPublicarAnuncio() {
    window.location.href = "publicarAnuncio.php?action=publicar";
}